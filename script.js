let currentQuestionIndex = 0;
let revealedAnswers = [];
let gameCompleted = false;

// DOM elements
const questionText = document.getElementById("questionText");
const questionBadge = document.getElementById("questionBadge");
const answersGrid = document.getElementById("answersGrid");
const nextButton = document.getElementById("nextButton");
const gameScreen = document.getElementById("gameScreen");
const completionScreen = document.getElementById("completionScreen");
const restartButton = document.getElementById("restartButton");

function loadQuestion() {
  const currentQuestion = sampleQuestions[currentQuestionIndex];

  // Update question text and badge
  questionText.textContent = currentQuestion.question;
  questionBadge.textContent = `Pregunta ${currentQuestionIndex + 1} de ${
    sampleQuestions.length
  }`;

  // Reset answer cards
  const answerCards = answersGrid.querySelectorAll(".answer-card");
  answerCards.forEach((card, index) => {
    card.classList.remove("hidden"); // Remove the hidden class
    card.classList.remove("revealed");
    card.innerHTML = `
            <div class="answer-content">
              <div class="question-mark">?</div>
              <p>ðŸ‘€</p>
            </div>
        `;

    // Add click event listener
    card.onclick = () => revealAnswer(index);
  });

    // Reset revealed answers for the new question
  revealedAnswers = [];

}

function revealAnswer(answerIndex) {
  if (revealedAnswers.includes(answerIndex)) return

  const currentQuestion = sampleQuestions[currentQuestionIndex]
  const answerCard = answersGrid.querySelector(`[data-index="${answerIndex}"]`)

  // Add to revealed answers
  revealedAnswers.push(answerIndex);

  // Update card appearance
  answerCard.classList.add("revealed");
  answerCard.innerHTML = `
        <div class="answer-content">
            <div class="checkmark"><img src="./IMG/ring.png" alt="O" class="ring"></div>
            <p class="answer-text">${currentQuestion.answers[answerIndex]}</p>
        </div>
    `;

}

function nextQuestion() {
  if (currentQuestionIndex < sampleQuestions.length - 1) {
    currentQuestionIndex++;
    loadQuestion();
  } else {
    // Game completed
    gameScreen.classList.add("hidden");
    completionScreen.classList.remove("hidden");
  }
}

function restartGame() {
  currentQuestionIndex = 0;
  revealedAnswers = [];
  gameCompleted = false;

  completionScreen.classList.add("hidden");
  gameScreen.classList.remove("hidden");

  loadQuestion();
}

// Event listeners
nextButton.addEventListener("click", nextQuestion);
restartButton.addEventListener("click", restartGame);

// Initialize game
loadQuestion();
